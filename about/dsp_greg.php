    <h2>Greg Sheppard</h2>
    <p class="subtitle">Director of Research</p>
    </header>
   	<img class="image left" src="images/people/greg.png"><p>Greg, our director of research, brings more than 30 years of technology experience to his market analysis work at Firsthand. His unique approach to market intelligence provides the Fund with visibility into emerging trends in the technology sector.</p> 
 	<p>Greg was a founder of the technology market intelligence firm iSuppli, which was sold to IHS in 2010. Prior to that he led the worldwide semiconductor group at Gartner/Dataquest. Greg also worked directly in the tech industry in engineering and strategic planning roles with GTE (now part of Verizon) and with Fairchild Semiconductor.</p>
  	<p>Greg Sheppard received his B.S. in Electrical Engineering/Computer Science from the University of Colorado. He received his M.S. in Systems Management from the University of Southern California.</p>
