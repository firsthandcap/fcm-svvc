	<h3>NAV Performance</h3>
	
    <p>The Fund's net asset value ("NAV") is calculated quarterly by dividing the total value of the portfolio by the number of shares outstanding. The Fund's NAV at inception (April 18, 2011) was $27.01; as of September 30, 2022, the NAV is $5.58. It is important to recognize that the NAV reflects the payment of cash distributions throughout the Fund's history. Each time a distribution is paid out, the NAV is lowered by the amount of the distribution. The Fund has paid out four distributions in its history: $0.32 in December 2013, $3.00 in November 2014, $2.86 in December 2014, and $0.03 in December 2018. If the NAV is adjusted to include these distributions, the NAV as of September 30, 2022, would be $7.38, which represents a 72.7% decline since inception. The chart below shows the difference between NAV and this adjusted NAV.</p> 
 
