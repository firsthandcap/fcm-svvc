        <h2>Hiku, Inc.</h2>
        <p class="subtitle">0.9% of portfolio as of 9/30/16</p>
        </header>
        
        <p>Hiku is an early-stage start-up that has developed an app for the iPhone and Android phones that lets users easily create shopping lists&#151;by voice command and by using a simple scanning device.</p> 

		<p>The scanner (which snaps to your refrigerator for easy access) recognizes barcodes, making it simple to wave it over any item and have it instantly added to a shopping list. Hiku is in the development stage of enabling online shopping with a wave of the device. </p>
        
        <p>In late 2015, the company announced its second-generation grocery scanner and integration with both Walmart and Peapod to enable shoppers to use the device to place orders directly with these merchants' online grocery services.</p>
