    <h2>Lyncean Technologies, Inc.</h2>
    <p class="subtitle">0.8% of portfolio as of 6/30/22</p>
    </header>
    
    <p>Lyncean Technologies develops compact accelerator-based light sources for commercial and research applications. The company was founded in 2001 to develop its compact light source (CLS), a miniature synchrotron x-ray source based on research performed at the SLAC National Accelerator Laboratory and Stanford University. The first commercial Lyncean CLS has been in operation at The Technical University of Munich (Germany) since 2015.
<br><br>
Lyncean's newest development is a novel extreme ultraviolet (EUV) light source, specifically designed for semiconductor lithography in high volume manufacturing environments.
<br><br>
In May 2018, the company announced the closing of $13.75 million in a Series B financing round with a syndicate that included Intel Capital as well as participation by previous investors EuroUS Ventures, AG, Anchor Fund, and Quintus Vermoegensverwaltung GmbH. Firsthand Technology Value Fund joined the Series B round in the third quarter of 2018.
