    <h2>Telepathy Investors, Inc.</h2>
    <p class="subtitle">0.0% of portfolio as of 9/30/20</p>
    </header>
    <p>Based in Tokyo, Telepathy is an early-stage company developing wearable communications devices for commercial and consumer markets. The company completed a successful Kickstarter campaign in April 2016, raising more than $124,000 to launch its wearable display, the Telepathy Walker.</p>
	<p>
	In the second quarter of 2019, Telepathy merged with Fairy Devices, another Japanese private company specializing in audio/voice-related artificial intelligence technologies. The stock-for-stock swap resulted in Telepathy Investors holding shares of Fairy Devices common stock.
	</p>
