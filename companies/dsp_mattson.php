    <h2>Mattson Technologies, Inc.</h2>
    <p class="subtitle">5.2% of portfolio as of 3/31/16</p>
    </header>
    <p>Mattson (MTSN) designs, manufactures, and markets wafer processing equipment for use in making semiconductor devices. The company specializes in equipment for dry strip, etch, and rapid thermal processing (RTP) steps in the semiconductor manufacturing process.</p>
    <p>The semiconductor equipment business has traditionally been a cyclical one. Spending on new manufacturing equipment tends to come in waves when manufacturers need to add additional capacity and/or transition to a new generation of manufacturing technologies. The industry is currently experiencing <strong>both</strong> capacity expansion and the early stages of a transition to so-called "sub-20-nanometer" devices. After enduring a costly corporate restructuring, Mattson appears well-positioned to benefit from the upswing in the semiconductor equipment business, particularly as some of its largest competitors wrestle with reorganizations of their own businesses.</p>
    
    <p><strong>News</strong></p>
    <p>On May 11, 2016, Mattson Technology announced the completion of its acquisition by Beijing E-Town Dragon Semiconductor Industry Investment Center. Under the terms of the merger agreement, Mattson stockholders will receive $3.80 per share in cash. As a result of the closing of the transaction, trading in the company's common stock has ceased. The Fund made its initial investment in Mattson in 2013. As of April 30, 2016, the Fund held 2,380,000 shares of Mattson common stock with a cost basis of approximately $2.41 per share. </p>

