        <h2>Hera Systems, Inc.</h2>
        <p class="subtitle">19.0% of portfolio as of 9/30/22</p>
        </header>
        
        <p>Hera Systems is a San Jose, California startup company developing nanosatellite technologies that will enable high-revisit, high-resolution imagery and video. The company's imaging satellites are designed to enable government and commercial customers to better monitor the ever-changing features of the earth's surface.</p>
      
        
        <h3>Recent News</h3>
         <p>In August 2022, Hera announced that it had been selected to provide spacecraft and related capabilities to the Tetra-5 program for the U.S. Space Force. The U.S. Space Force awarded the contract to lead the Tetra-5 program to Orion Space Solutions with support from Hera Systems. The contract award, if all options are exercised, is valued at up to $44.5 million.</p>
