        <h2>Turn Inc.</h2>
        <p class="subtitle">9.7% of portfolio as of 3/31/17</p>
        </header>
        <p>Turn, the largest independent company in the advertising technology sector and a recognized leader in platform-based mobile, video, display and Facebook advertising, is a late-stage private company that provides online advertising technology to Fortune 1000 marketers and their agencies. The company's software enables advertisers to engage in so-called "programmatic advertising," which is a term used to describe the purchase of digital advertising using computers, as opposed to human ad buyers and salespeople. Turn's advertising platform allows its customers to manage audience data, execute digital advertising campaigns, and track their effectiveness.</p> 

<p>In February 2016, it was announced that Turn had agreed to be acquired by Amobee, a subsidiary of Singtel, one of the largest mobile network operators in Singapore. You can read more about the acquisition <a href="../docs/press_022317.pdf" target="new">here</a>.</p>
       
       <p>In June 2017, the Fund announced that it received approximately $13.5 million in cash proceeds from the sale of Turn to Amobee. You can read the announcement <a href="../docs/press_060117.pdf" target="new">here</a>.
        
      
