        <h2>IntraOp Medical Corp.</h2>
    
        <p class="subtitle">54.0% of portfolio as of 9/30/22</p>
        </header>

       
       
<p>IntraOp is a private company that has developed a revolutionary medical device called the Mobetron, which utilizes electron linear accelerator (LINAC) technology to deliver electron beam radiation for the treatment of certain types of cancers.</p>

	<h3>Tackling a Big Problem</h3>
<p>Cancer is one of the leading causes of death worldwide. The National Cancer Institute (US) estimates that there will be more than 280,000 new cases of breast cancer detected in the U.S. in 2021, along with 149,500 cases of colorectal cancer and 60,430 cases of pancreatic cancer.</p> 

<p>In modern cancer treatment, nearly every leading cancer program uses some form of radiation to augment surgery. Historically, radiation treatment involved a time-consuming and imprecise external procedure that put a patient's healthy tissue and organs at greater risk. IntraOp's Mobetron revolutionized that traditional model by administering radiation during the surgical process, treatment known as intraoperative radiation therapy (IORT). It's safer, more accurate, and it saves the hospital and the patient a considerable amount of time. And time is money. Beyond that, clinical data confirms that successful outcomes with IORT are simply more consistent.</p>

<h3>Cancer-Killing Electrons</h3>
<p>IntraOp's Mobetron is the first mobile, self-shielded electron beam LINAC designed to deliver IORT to cancer patients during surgery. Significantly, the Mobetron is the only device of its kind that can be used in the operating room itself, eliminating the need to transport the patient during surgery to a neighboring shielded "bunker" to administer the electron IORT, which lowers the cost and the risk of infection.</p> 

<p>IntraOp received U.S. Food and Drug Administration (FDA) approval for the Mobetron in 1998, and in 2013 Medicare approved reimbursement for electron IORT.</p>

 <p><img class="image left" src="images/companies/intraop2.png" />  </p><p>Treatment with the Mobetron has been shown to be successful in the treatment of the following cancers:
<ul class="style4-alt">
	<li>Breast</li>
	<li>Skin</li>
	<li>Pancreatic</li>
	<li>Colorectal</li>
	<li>Gynecological</li>
	<li>Head and Neck</li>
	<li>Sarcomas</li>
	</ul>
	</p>
<p>The Mobetron is currently in use in more than 40 leading cancer centers worldwide, including the Cleveland Clinic, the Mayo Clinic, Stanford University Medical Center, the University of California at San Francisco, and Massachusetts General Hospital.</p>

	<h3>Recent News</h3>
<p>In November 2016, the company announced significant advances in the treatment of pancreatic cancer with IORT, which you can read more about <a href="http://www.prweb.com/releases/iort/electron/prweb13811241.htm" target="new">here</a>. The company announced in May 2019 the first patient enrollment in a pancreatic cancer trial led by Massachusetts General Hospital. Other leading institutions joining the trial include the Mayo Clinic, University of North Carolina, University of California at Irvine, and The Ohio State University.
</p>
<p>
In January 2020, IntraOp announced that a new Mobetron optimized to treat skin lesions is now being used to treat patients in a Florida dermatology clinic. Superficial electron therapy is recognized as standard of care treatment for Basal and Squamous Cell skin cancers by both the National Comprehensive Cancer Network (NCCN) as well as the American Society of Radiation Oncology (ASTRO). Per the guidelines, electron therapy may be used as an alternative to surgery or postoperatively. IntraOp's innovative electron beam technology provides sub-millimeter accuracy, and enables the Mobetron to deliver significantly less dose to the healthy tissues and organs that may exist below the tumor bed, as compared with x-ray or orthovoltage radiation.
</p>

<p>
In June 2020, the company announced a research and development collaboration with the Lausanne University Hospital (CHUV) to accelerate the development of FLASH radiotherapy toward first human trials. FLASH radiotherapy is a new form of cancer treatment that involves delivery of radiation doses in a few milliseconds, instead of minutes. IntraOp has also announced the installation of FLASH-equipped Mobetrons to The Ohio State University Comprehensive Cancer Center and the Centre hospitalier de l'Université de Montréal (CHUM) in Quebec.
</p>


