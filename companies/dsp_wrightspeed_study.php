	<h3>Recent News</h3>
<p>In August 2021, Wrightspeed announced a partnership with Matthews Specialty Vehicles to develop, convert, and deliver battery-powered and range-extended electric buses, trucks and specialty vehicles. The companies are currently fulfilling their first order to develop and produce 30 tactical Mobile Detection Systems (MDS) vehicles. The Wrightspeed/Matthews strategic collaboration aims to produce 5,000 vehicles over the next five years.</p>

<p>In March 2022, Wrightspeed announced a partnership with Romeo Power to deliver "Powertrain in a Crate<sup class="bc">TM</sup>" kits for medium- and heavy-duty trucks and buses. In May 2022, Wrightspeed announced that it has chagned its name to REVO Powertrains. </p>



