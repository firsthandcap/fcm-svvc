        <h2>Wrightspeed, Inc.</h2>
    
        <p class="subtitle">31.1% of portfolio as of 9/30/22</p>
        </header>

       
       
<p>Wrightspeed (DBA REVO Powertrains) provides electric drivetrains as retrofit kits to the heavy-duty truck market. The company's electric-powered drive system provides customers with exceptional efficiency, low emissions, and full power and torque. It utilizes plug-in grid power, regenerative braking, and an optional range extending engine to recharge the batteries. The Wrightspeed powertrain is best suited to trucks and buses that perform a large number of starts and stops in daily operation. We believe the tremendous potential fuel savings to the trucking industry represents a significant revenue opportunity for Wrightspeed. Wrightspeed, Inc. was founded by Ian Wright, a co-founder of Tesla Motors. </p>


	<p>Wrightspeed technology uses a geared traction drive (GTD<sup class="bc">TM</sup>) to deliver power to the wheels from a whisper-quiet electric motor instead of relying on combustion power, which translates into much quieter acceleration. The clutchless gearbox uses software to replace traditional transmission components, which reduces the truck's weight. The GTD magnifies the high torque of the electric motor, enabling quick starts even on hills and allowing efficient cruising speed on highways. </p>

<p>Wrightspeed's Route is a replacement powertrain that allows existing trucks to be retrofitted into electric vehicles, enabling companies to comply with government emission mandates without replacing entire fleets of gasoline/diesel-powered vehicles.</p>
</p>

<h3>Same Work, Half the Fuel</h3>
<p>The Route is available in two configurations (Route 500 and Route 1000) to power heavy-duty trucks. Each of these solutions enables fuel consumption reductions of more than 50%, compared with existing solutions using current technology.</p>

