    <p>Vufine, Inc. has developed a wearable high definition display that clips on to existing eyewear. The device enables users to have a heads-up display that connects to iOS, Android, and GoPro devices. The company completed a successful Kickstarter campaign in July 2015, raising more than $240,000 to launch its wearable display.</p>
    <p>In July 2016, the company responded to the popularity of its innovative heads-up display (HUD) among augmented reality gamers with the introduction of a new version of its product. The re-styled device is available in red, blue, green, and yellow to match the "team colors" found in Pokemon Go and Ingress, and features a larger screen mode suitable to portrait oriented applications.</p>
    <p>
In November 2016, Vufine completed a second successful Kickstarter campaign, this time raising more than $200,000 for its next-generation Vufine+.
</p>
    
   
