    <h2>Jawbone, Inc.</h2>
    <p class="subtitle">0.9% of portfolio as of 6/30/17</p>
    </header>
        
    <p>Jawbone was a late-stage private company in San Francisco that rose to prominence several years ago based on its popular Bluetooth headsets for cell phones. Until recently, it developed wearable activity-tracking wristbands and portable wireless speakers, while remaining a leader in the Bluetooth headset market.</p>
    
    <p>On July 13, 2017, we received a Notice of Assignment for the Benefit of Creditors from Aliphcom (dba Jawbone). The notice
		explains that Jawbone's assets are being liquidated and the company is being wound down.</p>
