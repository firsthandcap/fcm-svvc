    <h2>Closed-End Funds</h2>
    <p class="subtitle">Differences between closed- and open-end funds</p>
    </header>

    <p>A closed-end fund differs from a traditional open-end mutual fund in significant ways. As its name implies, closed-end funds are closed to new investment capital after the initial offering. That is, the number of shares is limited to the initial offering, unlike open-end funds, which create new shares whenever there is demand. Below are outlined some of the important differences between open- and closed-end funds.</p>
     
    <div class="grid-spaced"> 												
      <div class="row"> 													
          <div class="6u"><section> 									
              <h3>Closed-End Funds</h3>
                  <ul class="style4-alt">
                       <li>Fixed number of shares, following an initial public offering</li>
                        <li>Share price is determined by the market</li>
                        <li>Shares may trade at a premium or a discount to underlying NAV</li>
                        <li>Shares are bought and sold on secondary market, like stocks; fund not required to buy back shares from investors</li>
                        <li>Greater flexibility in illiquid investments</li>	
                   </ul>     																		
          </section></div>                         
          <div class="6u"><section> 																		
              <h3>Open-End Funds</h3>	
                  <ul class="style4-alt">					
                      <li>Continuous sale of shares; new shares are issued as demand warrants.</li>
                      <li>NAV is determined once per day at market close</li>
                      <li>Shares are bought and sold at NAV</li>
                      <li>Shares are bought and sold by the fund</li>
                      <li>Investments in illiquid securities are limited</li>
                  </ul>    
          </section></div> 
      </div>  
